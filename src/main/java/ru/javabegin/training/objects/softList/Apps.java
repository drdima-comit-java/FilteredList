package ru.javabegin.training.objects.softList;

public class Apps {
    private int appsId;
    private String appsName;
    private String appsPath;

    public Apps() {
    }

    public int getAppsId() {
        return appsId;
    }

    public void setAppsId(int appsId) {
        this.appsId = appsId;
    }

    public String getAppsName() {
        return appsName;
    }

    public void setAppsName(String appsName) {
        this.appsName = appsName;
    }

    public String getAppsPath() {
        return appsPath;
    }

    public void setAppsPath(String appsPath) {
        this.appsPath = appsPath;
    }
}
